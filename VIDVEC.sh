### Почему 13, а не 10
- **pacing** отделён от structure: structure = ЧТО происходит (арки, клиффхэнгеры), pacing = КАК БЫСТРО это происходит (45 страниц боевки vs 45 страниц тишины).
- **ensemble_dynamics** — критично для эталонов (Grand Blue, Jujutsu Kaisen): калоритные персонажи, которые ДОПОЛНЯЮТ друг друга = отдельная наука.
- **reader_psychology** — психология ЧИТАТЕЛЯ ≠ психология персонажей. Механики аддикции, антиутомление через 50+ глав, калибровка удивления, перечитываемость.

