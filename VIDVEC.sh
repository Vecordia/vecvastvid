## 7. СТАНДАРТ ПЕРСОНАЖА

Каждый персонаж = **реальный человек за аватаром** (как профессиональная D&D игра):

- **TOV (Tone of Voice)** — уникальный речевой паттерн. Нет двух одинаковых.
- **Архетип** — определяет реакции и решения
- **Мотивация** — явная цель + скрытая потребность
- **Боли/страхи** — определяют иррациональные реакции
- **Противоречия** — хочет одно, делает другое
- **Человечность** — злится не по делу, обижается, ошибается
- **Уникальная привычка** — жест, тик, манера
- **Юмор через TOV** — тип шуток определяется характером, а не сценаристом (саркастичный → сарказм, тупой → тупой юмор, умный → интеллектуальный)
- **Эволюция** — если арка требует, речь и поведение постепенно меняются

> Не должно выглядеть, что один сценарист пишет всем диалоги. Должно быть как будто каждый персонаж = отдельный человек, управляющий своим аватаром.

