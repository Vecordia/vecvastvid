| **Запуск** | `START_STUDIO.bat` → `desktop_app.py` → `src.main:app` |

### 10.2 Язык контента
- **Проза и диалоги:** Русский
- **JSON-ключи:** Английские
- **Промпты для генерации изображений (будущее):** Английский

### 10.3 Файловая иерархия проекта (`20_projects/[Название]/`)
```
01_lore/         — Персонажи, мировая библия, genre_rules.md
02_outlines/     — Аутлайны глав
03_chapters/     — Готовые главы
04_system_state/  — chapter_state.json, рекапы, регистры, chroma_db
05_archives/     — Сжатые старые главы, архив парсинга
06_references/   — Загруженные изображения и PDF
```

### 10.4 Pipeline конвейера
```
Триггер → Драматург (анализ арок) → Шоураннер (Outline) → Писатель (5 стр/чанк)
→ QA Syndicate (reject/pass) → Grammar Editor → Storyboarder → Compiler
→ Worldbuilder (пост-парсинг лора) → [Тройной Артефакт]
```

