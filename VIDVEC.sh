## 3. АНАЛИЗАТОР — ЯДРО КОНВЕЙЕРА

### Философия
Анализатор = генератор **ДНК идеальной манги**. Из любого входного файла он извлекает абстрактные МЕХАНИКИ (не сюжет, не персонажей, не конкретные сцены — только ПАТТЕРНЫ, формулы, структуры). Это конструктор, из которого агенты автоматически собирают уникальное произведение.

> **Паттерн ≠ плагиат.** Мы забираем только механики: КАК работает юмор, КАК строится напряжение, КАК вводится персонаж. Никогда — ЧТО именно говорят/делают конкретные персонажи.
> Паттерн ≠ шаблон. Паттерн = закон физики повествования. Говорит КАК работает механика, не ЧТО делать.

### Входные форматы и минимальный UI
Директор выбирает из 3 категорий при загрузке:

| Выбор | Форматы | Что анализатор делает |
|-------|---------|----------------------|
| **Сценарий** | `.txt`, `.pdf`, `.docx` | Текстовый анализ: диалоги, структура, психология, юмор, режиссура |
| **Исследование** | `.txt`, `.pdf`, `.docx` | Извлечение правил и формул. Research Routing (H-104): определяет фокус → приоритизирует релевантные экстракторы (×2 глубина) |
| **Манга (PDF)** | `.pdf` с визуалом | Vision Analysis + текстовый анализ: панели, визуальный язык, композиция, атмосфера |

Внутри каждой категории анализатор **сам** определяет жанр, тон, аудиторию и направляет в нужные экстракторы. Директор не должен разбираться в деталях — только выбрать 1 из 3.

> **Подтверждено:** Исследование Джимми Карра = 1 `.docx` файл. Система сама определит как «исследование по юмору» → приоритет экстракторам `humor` + `dialogue`.

### Правило неполной манги
Манга может приходить **не по порядку** и **не целиком**. Система:
- НЕ делает финальных выводов по одной главе
- Хранит `confidence_level` для паттерна: `preliminary` (1-3 главы) → `confirmed` (4+ глав)
- Обновляет и уточняет при поступлении новых глав
- Структурные паттерны (арки, pacing, глобальный сценарий) выставляются только после достаточного объёма

