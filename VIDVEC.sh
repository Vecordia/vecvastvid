## 5. СИСТЕМА ЖАНРОВ — 4-ОСЕВАЯ ТЕГОВАННАЯ МОДЕЛЬ

> **Принцип:** Вместо жёсткого деления «ситком / однолинейная» — гибкая 4-осевая система тегов. Любая манга = комбинация тегов. Это позволяет обрабатывать произведения, которые не вписываются в бинарную классификацию (H-102).

### Ось 1: Narrative Mode (как течёт история)

| Тег | Описание | Примеры |
|-----|----------|---------|
| `episodic` | Каждая глава автономна, микросюжет внутри глобальной линии | Grand Blue, Gintama (комедийные главы) |
| `linear` | Непрерывная линия от начала до конца | Frieren, Moriarty, Death Note |
| `hybrid` | Эпизодический старт → переход в линейный; или чередование | Gintama (серьёзные арки), Cowboy Bebop |

### Ось 2: Tone Tags (какие эмоции доминируют)

| Тег | Примеры |
|-----|---------|
| `comedy` | Grand Blue, Gintama |
| `drama` | Frieren, Великая небесная стена |
| `action` | Jujutsu Kaisen, Chainsaw Man |
| `horror` | Junji Ito, Gantz |
| `mystery` | Moriarty, Detective Conan |
| `romance` | Your Name (романтика как драйвер) |
| `psychological` | Death Note, Monster |
| `sci-fi` | Blame!, Planetes |
| `fantasy` | Frieren, Berserk |
| `post-apocalyptic` | Великая небесная стена |
| `slice_of_life` | Mushishi, Yotsuba |
| `dark_fantasy` | Berserk, Claymore |

> Можно ставить НЕСКОЛЬКО тегов: Gintama = `comedy` + `action` + `drama`

### Ось 3: Structure Pattern (как организованы главы)

| Тег | Описание |
|-----|----------|
| `monster_of_week` | Каждая глава — новый вызов/враг/задача |
| `arc_based` | Арки по 4-12 глав, каждая с завязкой-кульминацией-развязкой |
| `continuous` | Сплошной поток без чётких границ дуг |
| `anthology` | Разные истории в одной вселенной (Mushishi) |
| `investigation` | Каждая глава/арка = расследование с разгадкой |
| `tournament` | Серия боёв/соревнований с нарастающей сложностью |
| `parallel_stories` | Несколько параллельных линий, пересекающихся |

### Ось 4: Pacing Style (как быстро двигается)

| Тег | Описание |
|-----|----------|
| `slow_burn` | Медленное нарастание, капельное раскрытие |
| `escalation` | Постоянный рост ставок |
| `rhythmic` | Чередование: покой-взрыв-покой-взрыв |
| `breakneck` | Непрерывное движение без пауз |

### Примеры комбинаций

| Произведение | narrative_mode | tone_tags | structure_pattern | pacing_style |
|-------------|----------------|-----------|-------------------|--------------|
