## 8. РАБОЧИЙ ПРОЦЕСС

```
ЭТАП 1: Директор пишет идею (свободная форма)
ЭТАП 2: Система → 3 разных плана (мир + колоритные персонажи + арки + тон)
ЭТАП 3: Директор выбирает / редактирует / замечания → перегенерация
ЭТАП 4: Утверждённый план → ПРОДАКШН
ЭТАП 5: Система → 1 глава (~45 стр × 5-8 панелей)
ЭТАП 6: Директор ревью → замечания → переработка / одобрение
ЭТАП 7: Повтор с ЭТАПА 5
```

- **Мульти-проект:** работа над несколькими мангами одновременно с переключением
- **Narrative Mode** задаётся при создании проекта (linear/episodic)
- **Библиотека паттернов — глобальная**, агенты тянут автоматически
- **Бесшовное продолжение:** персонажи, тон, сюжетные нити — всё консистентно на 150-й главе как на 1-й

